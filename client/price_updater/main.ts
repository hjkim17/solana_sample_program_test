import {
  establishConnection,
  establishPayer,
  checkProgram,
  updatePrice,
} from './caller';

async function main() {

  // Establish connection to the cluster
  await establishConnection();

  // Determine who pays for the fees
  await establishPayer();

  // Check if the program has been deployed
  await checkProgram();

  await updatePrice(1234);

  console.log('Success');
}

main().then(
  () => process.exit(),
  err => {
    console.error(err);
    process.exit(-1);
  },
);
